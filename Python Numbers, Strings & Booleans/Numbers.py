# int
a = 2
b = 54654654545
c = -23135
print(a)
print(b)
print(c)
print('--------------------------------------------')
# float
x = 2.5
y = 12.65464646
z = -123.8156
print(x)
print(y)
print(z)
print('---------------------------------------------')
# complex
p = 3 + 2j
q = 5j
r = -2j
print(p)
print(q)
print(r)

# Float can also be scientific numbers with an "e" to indicate the power of 10.
x = 35e3
y = 12E4
z = -87.7e100
print(x)
print(y)
print(z)
print(type(x))


# Type Conversion
x = 1    # int
y = 2.8  # float
z = 1j   # complex

# convert from int to float:
a = float(x)

# convert from float to int:
b = int(y)

# convert from int to complex:
c = complex(x)

print(a)
print(b)
print(c)



